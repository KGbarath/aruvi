public class p26 {
    public static void main(String[] args) {
      // Conversion from kilometers to miles
      double kilometers = 100;
      double miles = kilometers * 0.621371;
      System.out.println(kilometers + " kilometers is equivalent to " + miles + " miles.");
      
      // Conversion from Celsius to Fahrenheit
      double celsius = 25;
      double fahrenheit = (celsius * 9 / 5) + 32;
      System.out.println(celsius + "°C is equivalent to " + fahrenheit + "°F.");
      
      // Conversion from square feet to acres
      double sqft = 43560;
      double acre = sqft / 43560;
      System.out.println(sqft + " square feet is equivalent to " + acre + " acres.");
    }
  }
  
