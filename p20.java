public class p20 {
    public static void main(String[] args) {
      int x = 10;
      System.out.println("x: " + x); // x: 10
      System.out.println("Prefix increment: " + (++x)); // x: 11
      x = 10;
      System.out.println("x: " + x); // x: 10
      System.out.println("Postfix increment: " + (x++)); // x: 10
      System.out.println("x: " + x); // x: 11
    }
  }
  
